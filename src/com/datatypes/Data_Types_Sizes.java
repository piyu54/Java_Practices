package com.datatypes;

public class Data_Types_Sizes {

	public static void main(String[] args) {
		
		System.out.println("-------------------------Primitive data types sizes-------------------------");
		System.out.println("byte size "+Byte.BYTES+" Byte And "+Byte.SIZE+" bits");
		System.out.println("short size "+Short.BYTES+" Byte And "+Short.SIZE+" bits");
		System.out.println("integer size "+Integer.BYTES+" Byte And "+Integer.SIZE+" bits");
		System.out.println("long size "+Long.BYTES+" Byte And "+Long.SIZE+" bits");
		System.out.println("float size "+Float.BYTES+" Byte And "+Float.SIZE+" bits");
		System.out.println("double size "+Double.BYTES+" Byte And "+Double.SIZE+" bits");
		System.out.println("char size "+Character.BYTES+" Byte And "+Character.SIZE+" bits");
		
	}

}
