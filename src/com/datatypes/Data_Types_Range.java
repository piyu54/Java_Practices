package com.datatypes;

public class Data_Types_Range {
	public static void main(String[] args) {
		
		System.out.println("-------------------------Primitive data types Ranges-------------------------");
		System.out.println("byte Range : "+Byte.MIN_VALUE +" to "+Byte.MAX_VALUE);
		System.out.println("short Range : "+Short.MIN_VALUE +" to "+Short.MAX_VALUE);
		System.out.println("byte Range : "+Integer.MIN_VALUE +" to "+Integer.MAX_VALUE);
		System.out.println("integer Range : "+Long.MIN_VALUE +" to "+Long.MAX_VALUE);
		System.out.println("float Range : "+Float.MIN_VALUE +" to "+Float.MAX_VALUE);
		System.out.println("double Range : "+Double.MIN_VALUE +" to "+Double.MAX_VALUE);
		System.out.println("char Range : "+(int)Character.MIN_VALUE+" to "+(int)Character.MAX_VALUE);
		System.out.println("Boolean range is not fixed....");// jvm handles  (return true or flase)
	}

}
