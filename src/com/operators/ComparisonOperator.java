package com.operators;

public class ComparisonOperator {

	public static void main(String[] args) {

//		>,<,>=,<=,==,!=

		int a = 9;
		int b = 19;
		System.out.println("---------------Relation / Comparsion Operator");
		System.out.println(a + " is greater than " + b + " " + (a > b));
		System.out.println(a + " is smaller than " + b + " " + (a < b));
		System.out.println(a + " is greater than or equal to " + b + " " + (a >= b));
		System.out.println(a + " is smaller than or equal to " + b + " " + (a <= b));
		System.out.println(a + " is equals to " + b + " " + (a == b));
		System.out.println(a + " is not equals to " + b + " " + (a != b));
	}

}
