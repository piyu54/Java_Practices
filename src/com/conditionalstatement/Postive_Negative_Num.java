package com.conditionalstatement;

public class Postive_Negative_Num {

	public static void main(String[] args) {
		int num =9;
		
		if(num>0)
		{
			System.out.println("this is an postive number");
		}
		else if (num<0)
		{
			System.out.println("this is an negative number");
		}
		else
		{
			System.out.println("this is an zero");
		}

	}

}
