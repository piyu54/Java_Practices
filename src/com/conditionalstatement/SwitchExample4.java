package com.conditionalstatement;

public class SwitchExample4 {

	public static void main(String[] args) {

		String name = "sakshue";

		switch (name) {
		case "piyu":
			System.out.println("my name is piyu");
			break;

		case "sakshu":
			System.out.println("my name is sakshu");
			break;

		case "pritu":
			System.out.println("my name is pritu");
			break;

		default:
			System.out.println("you enter wrong name");
			break;

		}
	}

}
