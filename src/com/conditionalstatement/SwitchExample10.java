package com.conditionalstatement;

public class SwitchExample10 {
//6. Take a single character input and check whether it is a vowel or consonant using a switch statement.

	public static void main(String[] args) {

		// a,e,i,o,u

		char vowel = 'p';

		switch (vowel) {

		case 'a':
			System.out.println("This is an vowel");
			break;

		case 'e':
			System.out.println("This is an vowel");
			break;
		case 'i':
			System.out.println("This is an vowel");
			break;
		case 'o':
			System.out.println("This is an vowel");
			break;
		case 'u':
			System.out.println("This is an vowel");
			break;
		default:
			System.out.println("This is not an vowel");

		}
	}

}
