package com.array;

public class IntDynamic {

	public static void main(String[] args) {
		
		
		System.out.println("Dynamic way for integer");
		
		int[] marks= new int[4];
		marks[0]=9;
		marks[1]=8;
		marks[2]=9;
		marks[3]=5;
		
		for(int i=0;i<marks.length;i++)
		{
			System.out.println(marks[i]);
		}
		
		
	}
	
}
